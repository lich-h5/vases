/**
 * 取两个个颜色之间的过渡色
 * @param startColor 开始颜色hex
 * @param endColor 结束颜色hex
 * @param count 生产几个过渡色
 * @returns {[]}
 * example:
 *   colorArr = gradientColor(startColor,endColor,5);
 */
export function gradientColor(startColor, endColor, count) {
    let startRGB = hexToRgb(startColor); //转换为rgb数组模式
    let startR = startRGB[0];
    let startG = startRGB[1];
    let startB = startRGB[2];

    let endRGB = hexToRgb(endColor);
    let endR = endRGB[0];
    let endG = endRGB[1];
    let endB = endRGB[2];

    let sR = (endR - startR) / count; //总差值
    let sG = (endG - startG) / count;
    let sB = (endB - startB) / count;

    let colorArr = [];
    for (let i = 0; i < count; i++) {
        //计算每一步的hex值
        let hex = rgbToHex('rgb(' + parseInt((sR * i + startR)) + ',' + parseInt((sG * i + startG)) + ',' +
            parseInt((sB * i + startB)) + ')');
        colorArr.push(hex);
    }
    return colorArr;
}

/**
 * 将颜色的 hex 表示方式转换为 RGB 表示方式
 * 参考：http://www.w3.org/TR/css3-color/#svg-color
 * @param sColor
 * @returns {string|[]} rgb数组
 */
export function hexToRgb(sColor) {
    const reg = /^#([0-9a-fA-f]{3}|[0-9a-fA-f]{6})$/;
    sColor = sColor.toLowerCase();

    //颜色常量转换
    if (sColor.slice(0, 1) !== "#") {
        for (let name in colorEnum) {
            if (name === sColor) {
                sColor = "#" + colorEnum[name];
            }
        }
    }
    if (sColor && reg.test(sColor)) {
        if (sColor.length === 4) {
            let sColorNew = "#";
            for (let i = 1; i < 4; i += 1) {
                sColorNew += sColor.slice(i, i + 1).concat(sColor.slice(i, i + 1));
            }
            sColor = sColorNew;
        }
        //处理6位的颜色值
        let sColorChange = [];
        for (let i = 1; i < 7; i += 2) {
            sColorChange.push(parseInt("0x" + sColor.slice(i, i + 2)));
        }
        return sColorChange;
    } else {
        return sColor;
    }
}

/**
 * 将颜色的 RGB 表示方式转换为 Hex 表示方式
 * @param rgb
 * @returns {string|*}
 * @constructor
 */
export function rgbToHex(rgb) {
    let _this = rgb;
    let reg = /^#([0-9a-fA-f]{3}|[0-9a-fA-f]{6})$/;
    if (/^(rgb|RGB)/.test(_this)) {
        let aColor = _this.replace(/(?:\(|\)|rgb|RGB)*/g, "").split(",");
        let strHex = "#";
        for (let i = 0; i < aColor.length; i++) {
            let hex = Number(aColor[i]).toString(16);
            hex = hex < 10 ? 0 + '' + hex : hex; // 保证每个rgb的值为2位
            if (hex === "0") {
                hex += hex;
            }
            strHex += hex;
        }
        if (strHex.length !== 7) {
            strHex = _this;
        }
        return strHex;

    } else if (reg.test(_this)) {
        let aNum = _this.replace(/#/, "").split("");
        if (aNum.length === 6) {
            return _this;
        } else if (aNum.length === 3) {
            let numHex = "#";
            for (let i = 0; i < aNum.length; i += 1) {
                numHex += (aNum[i] + aNum[i]);
            }
            return numHex;
        }
    } else {
        return _this;
    }
}

